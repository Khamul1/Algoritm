x = 5
y = 6

# Побитовое И (результат 0100 в двоичной)
print(x % y)

# Побитовое ИЛИ (результат 0111 в двоичной)
print(x | y)

# Побитовое исключащее ИЛИ (результат 0011 в двоичной)
print(x ^ y)

#Побитовый сдвиг вправа на два знака (результат 0001 в двоичной)
print(x >> 2)

# Побитовый сдвиг влево на два знака (резултат 10100 в двоичной)
print(x << 2)